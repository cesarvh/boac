<template>
  <div class="m-3">
    <Spinner :is-plural="true" alert-prefix="Search results" />
    <div v-if="!loading && !results.totalStudentCount && !results.totalCourseCount && !results.totalAdmitCount && !size(results.notes) && !size(results.appointments)">
      <h1
        id="page-header-no-results"
        class="page-section-header"
        aria-live="polite">
        No results<span v-if="phrase"> matching '{{ phrase }}'</span>
      </h1>
      <div>Suggestions:</div>
      <ul>
        <li>Keep your search term simple.</li>
        <li>Check your spelling and try again.</li>
        <li>Search classes by section title, e.g., <strong>AMERSTD 10</strong>.</li>
        <li>Avoid using generic terms, such as <strong>test</strong> or <strong>enrollment</strong>.</li>
        <li>Longer search terms may refine results; <strong>registration fees</strong> instead of <strong>registration</strong>.</li>
        <li>Abbreviations of section titles may not return results; <strong>COMPSCI 161</strong> instead of <strong>CS 161</strong>.</li>
      </ul>
    </div>
    <div
      v-if="!loading && size(results.admits)"
      tabindex="0">
      <h1 id="admit-results-page-header" class="page-section-header">
        {{ 'admitted student' | pluralize(results.totalAdmitCount) }}<span v-if="phrase">  matching '{{ phrase }}'</span>
      </h1>
      <div v-if="size(results.admits) < results.totalAdmitCount">
        Showing the first {{ size(results.admits) }} admitted students.
      </div>
      <AdmitDataWarning :updated-at="get(results.admits, '[0].updatedAt')" />
      <div>
        <SortableAdmits :admitted-students="results.admits" />
      </div>
    </div>
    <div
      v-if="!loading && results.totalStudentCount"
      tabindex="0">
      <h1 id="student-results-page-header" class="page-section-header">
        {{ 'student' | pluralize(results.totalStudentCount) }}<span v-if="phrase">  matching '{{ phrase }}'</span>
      </h1>
      <div v-if="results.totalStudentCount > studentLimit">
        Showing the first {{ studentLimit }} students.
      </div>
    </div>
    <div v-if="!loading && results.totalStudentCount" class="cohort-column-results">
      <div class="search-header-curated-cohort">
        <CuratedGroupSelector
          context-description="Search"
          :ga-event-tracker="$ga.searchEvent"
          :students="results.students" />
      </div>
      <div>
        <SortableStudents :students="results.students" :options="studentListOptions" />
      </div>
    </div>
    <div v-if="!loading && results.totalCourseCount" class="pt-4">
      <SortableCourseList
        :search-phrase="phrase"
        :courses="results.courses"
        :total-course-count="results.totalCourseCount"
        :render-primary-header="!results.totalStudentCount && !!results.totalCourseCount && !size(results.notes)" />
    </div>
    <div v-if="!loading && size(results.notes)" class="pt-4">
      <h2
        id="search-results-category-header-notes"
        class="page-section-header">
        {{ size(results.notes) }}{{ completeNoteResults ? '' : '+' }}
        {{ size(results.notes) === 1 ? 'advising note' : 'advising notes' }}
        <span v-if="phrase"> with '{{ phrase }}'</span>
      </h2>
      <AdvisingNoteSnippet
        v-for="advisingNote in results.notes"
        :key="advisingNote.id"
        :note="advisingNote" />
      <div class="text-center">
        <b-btn
          v-if="!completeNoteResults"
          id="fetch-more-notes"
          variant="link"
          @click.prevent="fetchMoreNotes()">
          Show additional advising notes
        </b-btn>
        <SectionSpinner :loading="loadingAdditionalNotes" name="Notes" />
      </div>
    </div>
    <div v-if="!loading && size(results.appointments)" class="pt-4">
      <h2
        id="search-results-category-header-appointments"
        class="page-section-header">
        {{ size(results.appointments) }}{{ completeAppointmentResults ? '' : '+' }}
        {{ size(results.appointments) === 1 ? 'advising appointment' : 'advising appointments' }}
        <span v-if="phrase"> with '{{ phrase }}'</span>
      </h2>
      <AppointmentSnippet
        v-for="appointment in results.appointments"
        :key="appointment.id"
        :appointment="appointment" />
      <div class="text-center">
        <b-btn
          v-if="!completeAppointmentResults"
          id="fetch-more-appointments"
          variant="link"
          @click.prevent="fetchMoreAppointments()">
          Show additional advising appointments
        </b-btn>
        <SectionSpinner :loading="loadingAdditionalAppointments" name="Appointments" />
      </div>
    </div>
  </div>
</template>

<script>
import AdmitDataWarning from '@/components/admit/AdmitDataWarning';
import AdvisingNoteSnippet from '@/components/search/AdvisingNoteSnippet';
import AppointmentSnippet from '@/components/search/AppointmentSnippet';
import Context from '@/mixins/Context';
import CuratedGroupSelector from '@/components/curated/CuratedGroupSelector';
import Loading from '@/mixins/Loading';
import SectionSpinner from '@/components/util/SectionSpinner';
import SortableAdmits from '@/components/admit/SortableAdmits';
import SortableCourseList from '@/components/course/SortableCourseList';
import SortableStudents from '@/components/search/SortableStudents';
import Spinner from '@/components/util/Spinner';
import Util from '@/mixins/Util';
import { search, searchAdmittedStudents } from '@/api/search';

export default {
  name: 'Search',
  components: {
    AdmitDataWarning,
    AdvisingNoteSnippet,
    AppointmentSnippet,
    CuratedGroupSelector,
    SectionSpinner,
    SortableAdmits,
    SortableCourseList,
    SortableStudents,
    Spinner
  },
  mixins: [Context, Loading, Util],
  data: () => ({
    appointmentOptions: {
      limit: 20,
      offset: 0
    },
    loadingAdditionalAppointments: undefined,
    loadingAdditionalNotes: undefined,
    noteAndAppointmentOptions: {
      advisorCsid: undefined,
      advisorUid: undefined,
      studentCsid: undefined,
      topic: undefined,
      dateFrom: undefined,
      dateTo: undefined,
    },
    noteOptions: {
      limit: 20,
      offset: 0
    },
    phrase: null,
    results: {
      admits: null,
      appointments: null,
      courses: null,
      notes: null,
      students: null,
      totalCourseCount: null,
      totalStudentCount: null
    },
    studentLimit: 50,
    studentListOptions: {
      includeCuratedCheckbox: true,
      sortBy: 'lastName',
      reverse: false
    }
  }),
  computed: {
    completeAppointmentResults() {
      return this.size(this.results.appointments) < this.appointmentOptions.limit + this.appointmentOptions.offset;
    },
    completeNoteResults() {
      return this.size(this.results.notes) < this.noteOptions.limit + this.noteOptions.offset;
    }
  },
  mounted() {
    this.phrase = this.$route.query.q;
    const includeAdmits = this.$route.query.admits;
    const includeCourses = this.$route.query.courses;
    const includeNotes = this.$route.query.notes;
    const includeStudents = this.$route.query.students;
    if (includeNotes) {
      this.noteAndAppointmentOptions.advisorCsid = this.$route.query.advisorCsid;
      this.noteAndAppointmentOptions.advisorUid = this.$route.query.advisorUid;
      this.noteAndAppointmentOptions.studentCsid = this.$route.query.studentCsid;
      this.noteAndAppointmentOptions.topic = this.$route.query.noteTopic;
      this.noteAndAppointmentOptions.dateFrom = this.$route.query.noteDateFrom;
      this.noteAndAppointmentOptions.dateTo = this.$route.query.noteDateTo;
    }
    if (this.phrase || includeNotes) {
      this.alertScreenReader(`Searching for '${this.phrase}'`);
      let queries = [];
      if (includeCourses || includeNotes || includeStudents) {
        queries.push(
          search(
            this.phrase,
            this.isNil(includeNotes) ? false : includeNotes,
            this.isNil(includeCourses) ? false : includeCourses,
            this.isNil(includeNotes) ? false : includeNotes,
            this.isNil(includeStudents) ? false : includeStudents,
            this.extend({}, this.noteAndAppointmentOptions, this.appointmentOptions),
            this.extend({}, this.noteAndAppointmentOptions, this.noteOptions)
          )
        );
      }
      if (includeAdmits) {
        queries.push(searchAdmittedStudents(this.phrase));
      }
      Promise.all(queries).then(responses => {
        this.each(responses, (response) => this.merge(this.results, response));
        this.each(this.results.students, student => {
          student.alertCount = student.alertCount || 0;
          student.term = student.term || {};
          student.term.enrolledUnits = student.term.enrolledUnits || 0;
        });
      })
      .then(() => {
        this.loaded('Search results', );
        const totalCount =
          this.toInt(this.results.totalCourseCount, 0) +
          this.toInt(this.results.totalStudentCount, 0);
        const focusId = totalCount ? 'page-header' : 'page-header-no-results';
        this.putFocusNextTick(focusId);
        this.$ga.searchEvent(
          `Search phrase: '${this.phrase || ''}'`,
          `Search with admits: ${this.includeAdmits}; courses: ${this.includeCourses}; notes: ${this.includeNotes}; students: ${this.includeStudents}`);
      });
    } else {
      this.$router.push({ path: '/' }, this.noop);
    }
  },
  methods: {
    fetchMoreAppointments() {
      this.appointmentOptions.offset = this.appointmentOptions.offset + this.appointmentOptions.limit;
      this.appointmentOptions.limit = 20;
      this.loadingAdditionalAppointments = true;
      search(
        this.phrase,
        true,
        false,
        false,
        false,
        this.extend({}, this.noteAndAppointmentOptions, this.appointmentOptions),
        null
      )
        .then(data => {
          this.results.appointments = this.concat(this.results.appointments, data.appointments);
          this.loadingAdditionalAppointments = false;
        });
    },
    fetchMoreNotes() {
      this.noteOptions.offset = this.noteOptions.offset + this.noteOptions.limit;
      this.noteOptions.limit = 20;
      this.loadingAdditionalNotes = true;
      search(
        this.phrase,
        false,
        false,
        true,
        false,
        null,
        this.extend({}, this.noteAndAppointmentOptions, this.noteOptions)
      )
        .then(data => {
          this.results.notes = this.concat(this.results.notes, data.notes);
          this.loadingAdditionalNotes = false;
        });
    }
  }
};
</script>

